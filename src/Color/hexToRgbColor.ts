import type {RgbColor} from './Color.interfaces';

/**
 * Converts a given hex color #ffffff or #fff to it's rgb representation
 *
 * The base implementation is from: https://stackoverflow.com/a/5624139
 *
 * @param hex
 */
export function hexToRgbColor(hex: string): RgbColor
{
    // Expand shorthand form (e.g. "03F") to full form (e.g. "0033FF")
    var shorthandRegex = /^#?([a-f\d])([a-f\d])([a-f\d])$/i;
    hex = hex.replace(shorthandRegex, (_, r, g, b) => r + r + g + g + b + b);
    
    var result = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i.exec(hex);
    if (result) {
        return {
            r: parseInt(result[1], 16),
            g: parseInt(result[2], 16),
            b: parseInt(result[3], 16)
        };
    }
    throw new Error('Invalid hex color given: ' + hex);
}